package code03;

public class PrimitivesPractice {

    public static void main(String[] args) {

        //create variable

        int catCount = 20;


        byte letterCount = 26;
        System.out.println( "The letter count is " + letterCount );

        short sheepCount = 20;
        System.out.println(" The sheep count is " + sheepCount);

        long mileToMoon = 5000000l;
        System.out.println(" The mile is to moon " + mileToMoon);

        long mileToSun = 100000000L;
        System.out.println( " The mile is to sun " + mileToSun);

        // --------------- floating point ------------
        // add f or F after number
        float priceOfBanana = 1.99f;
        System.out.println(" The price of banana " + priceOfBanana);

        float priceOfPotato = 2.49F;
        System.out.println(" The price of potato " + priceOfPotato );

        // ----------------- larger floating point numbers --------
        double priceOfIpad =355.99d ;
        System.out.println(" The price of Ipad " + priceOfIpad);
        double priceOfIpadPro =1024.99D ;
        double priceOfMac = 2299.99 ;

        // if you have a whole number by itself 10 or 100 compiler automatically assume it's an int
        // if you have a fractional number by itself, compiler automatically assume it's an double











    }
}
