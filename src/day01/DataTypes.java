package day01;

public class DataTypes {
    public static void main(String[] args) {
        double d = 456.5;
        System.out.println(d);
        float f = 23.9f;
        int age = 45;
        long size = 2345;

        System.out.println(f);
        System.out.println(age);
        System.out.println(size);

        // + - / * %
        // 10/9 == 1
        int r = 10 % 7;
        System.out.println(r);
    }
}
